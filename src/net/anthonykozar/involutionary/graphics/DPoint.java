/*	DPoint.java
	
	A pair of double-precision coordinates representing a point in a
	plane using rectangular coordinates.
 */

package net.anthonykozar.involutionary.graphics;

public class DPoint
{
	public double	x;
	public double	y;
	
	public DPoint()
	{
		// no init necessary
	}

	public DPoint(double xcoord, double ycoord)
	{
		x = xcoord;
		y = ycoord;
	}

}
